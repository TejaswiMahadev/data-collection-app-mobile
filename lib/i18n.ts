export type Language = 'en' | 'hi' | 'od';

export function t(key: TranslationKey, lang: Language = 'en'): string {
  const primaryVal = translations[lang]?.[key] || (translations.en as any)[key] || key;
  const primary = Array.isArray(primaryVal) ? primaryVal[0] : primaryVal as string;

  if (lang === 'en') return primary;

  const secondaryVal = translations.en[key];
  const secondary = Array.isArray(secondaryVal) ? secondaryVal[0] : secondaryVal as string;

  if (!secondary || primary === secondary) return primary;

  return `${primary} (${secondary})`;
}

export function getTips(lang: Language = 'en'): string[] {
  return (translations[lang] as any)?.tips || translations.en.tips || [];
}

